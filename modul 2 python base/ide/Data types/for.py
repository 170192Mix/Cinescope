# Задание 1
for number in range(1, 6): # range(1, 6) — создаёт последовательность чисел от 1 до 5 (6 не включается).
    print(f"{number} в квадрате = {number ** 2}") # number ** 2 — возводит число в квадрат, f"{...}" — форматированная строка для красивого вывода

# Задание 2
n = 10

# Вычисляем сумму чисел от 1 до n
total = 0
for i in range(1, n + 1): # range(1, n + 1) — создаёт последовательность от 1 до n включительно.
    total += i # total += i — накапливает сумму

# Выводим результат
print("Сумма чисел от 1 до", n, "равна", total)

