# Задание 5
numbers = [3, 8, 1, 9, 4]
maximum = max(numbers)
print("Максимальное число:", maximum)

# Через цикл for
numbers = [3, 8, 1, 9, 4]
maximum = numbers[0]  # Начинаем с первого элемента

for num in numbers:
    if num > maximum:
        maximum = num

print("Максимальное число:", maximum)

# Задание 6
# Словарь с продуктами и ценами
products = {"яблоко": 100, "банан": 50, "апельсин": 70}

# Считаем сумму всех значений (цен)
total = sum(products.values()) # products.values() — возвращает список всех значений, sum считает список

# Выводим результат
print("Сумма всех цен:", total)