# Списки (list)
# Задание 1
colors = ["red", "green", "blue"]
print("ID до изменений:", id(colors)) # Проверка идентификатора до изменений

colors.append("yellow") # Добавление элемента
colors.remove("green") # Удаление элемента

print("ID до изменений:", id(colors)) # Проверка идентификатора до изменений
print("Итоговый результат:", colors)

