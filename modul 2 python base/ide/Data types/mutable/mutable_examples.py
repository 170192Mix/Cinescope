# Словари
person = {"name": "Alice", "age": 25}
print("ID до изменений:", id(person)) # Проверка идентификатора до изменений

person["age"] = 30 # Изменение значения по ключу

print("ID после изменений:", id(person)) # Проверка идентификатора после изменений
print("Итоговый словарь:", person)

