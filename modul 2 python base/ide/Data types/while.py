# Задание 1
# Начальное значение
number = 1

# Цикл while: пока number не больше 10
while number <= 10: # выполняем цикл, пока число не станет больше 10
    print(number)
    number += 1  # увеличиваем число на 1

# Задание 2
n = 10

# Инициализируем счётчик и сумму
i = 1 # начинаем с 1
total = 0 # начальная сумма

# Цикл while для подсчёта суммы от 1 до n
while i <= n: # пока i не больше n, прибавляем i к сумме
    total += i
    i += 1 # увеличиваем счётчик на каждой итерации

# Выводим результат
print("Сумма чисел от 1 до", n, "равна", total)

# Задание 3
nums = [2, 7, 4, 9, 6, 5]

# Индекс для прохода по списку
i = 0

# Пока индекс меньше длины списка
while i < len(nums): # используем while, потому что при удалении элементов длина списка меняется
    if nums[i] % 2 == 0:
        # Если элемент чётный — удаляем его
        nums.pop(i) # удаляет элемент с текущим индексом
        # Не увеличиваем индекс, т.к. список сдвинулся
    else:
        # Если нечётный — переходим к следующему
        i += 1

# Выводим результат
print("Список без чётных чисел:", nums)